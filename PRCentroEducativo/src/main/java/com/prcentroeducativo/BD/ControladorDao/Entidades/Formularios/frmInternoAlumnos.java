    private Alumno getCampos(){
        Alumno alum=new Alumno();
        alum.setNombre(txtNombre2.getText());
        alum.setId(Integer.parseInt(txtId.getText()));
        alum.setApellido2(jtxtApellido2.getText());
        alum.setCp(Integer.parseInt(jtxtCp.getText()));
        alum.setDni(jtxtDni.getText());
        alum.setFnacimiento(Date.valueOf(jtxtFechaNac.getText()));
        alum.setTelefono(Integer.parseInt(jtxtTelefono.getText()));
        alum.setEmail(jtxtEmail.getText());
        alum.setDireccion(jtxtDireccion.getText());
        alum.setPoblacion(jtxtPoblacion.getText());
        alum.setProvincia(jtxtProvincia.getText());
        
        return alum;        
    }
    
